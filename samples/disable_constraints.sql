DECLARE CURSOR CCONST IS SELECT
	CONSTRAINT_NAME,
	TABLE_NAME
FROM
	USER_CONSTRAINTS
WHERE
	CONSTRAINT_TYPE = 'R'
	AND STATUS = 'ENABLED';

BEGIN
	FOR I IN CCONST LOOP
		BEGIN
			EXECUTE IMMEDIATE 'ALTER TABLE ' || I.TABLE_NAME || ' DISABLE CONSTRAINT ' || I.CONSTRAINT_NAME || '';
			EXCEPTION
			WHEN OTHERS THEN DBMS_OUTPUT.PUT_LINE ('PROBLEM DISABLING: ' || I.TABLE_NAME || '.' || I.CONSTRAINT_NAME || ': ' || SQLERRM);
		END;
	END LOOP;
END;